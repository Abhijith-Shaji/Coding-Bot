
public class Animal {
	
	public void makeSound() {
		
    System.out.println("The animal makes a sound");
  }
}

  public class Dog extends Animal {
	  
	
  public void makeSound() {
	  
    System.out.println("The dog barks");
  }
}

    public class Cat extends Animal {
	
  public void makeSound() {
	  
    System.out.println("The cat meows");
  }
}

 